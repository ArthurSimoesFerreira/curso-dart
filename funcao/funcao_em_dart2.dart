main(){

  var adicao = (int a, int b) {
    return a + b;
  };

  var subtracao = (int a, int b) => a - b;

  var divisao = (int a, int b) => a/b;

  var multiplicacao = (int a, int b) => a * b;

  print(adicao(4, 19));
  print(subtracao(9, 3));
  print(divisao(7, 3));
  print(multiplicacao(43, 50));
}